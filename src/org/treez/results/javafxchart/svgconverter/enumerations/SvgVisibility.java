package org.treez.results.javafxchart.svgconverter.enumerations;

/**
 * Represents the visibility of a svg node, see https://developer.mozilla.org/en-US/docs/Web/SVG/Attribute/visibility
 */
public enum SvgVisibility {

	//#region VALUES

	/**
	 * The node is visible
	 */
	VISIBLE,

	/**
	 * The node is not visible
	 */
	HIDDEN,

	/**
	 * The node is collapsed
	 */
	COLLAPSE,

	/**
	 * The node uses the visibility of its parent node
	 */
	INHERIT;

	//#end region

	//#region ATTRIBUTES

	//#end region

	//#region CONSTRUCTORS

	//#end region
}
