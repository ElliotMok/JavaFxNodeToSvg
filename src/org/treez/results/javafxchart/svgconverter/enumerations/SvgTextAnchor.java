package org.treez.results.javafxchart.svgconverter.enumerations;

/**
 * Defines how text is aligned, see https://developer.mozilla.org/en-US/docs/Web/SVG/Attribute/text-anchor
 */
public enum SvgTextAnchor {

	//#region VALUES

	/**
	 * left anchor
	 */
	LEFT,

	/**
	 * middle anchor
	 */
	MIDDLE,

	/**
	 * end anchor
	 */
	END,

	/**
	 * use text anchor of parent
	 */
	INHERIT;

	//#end region

	//#region ATTRIBUTES

	//#end region

	//#region CONSTRUCTORS

	//#end region
}
